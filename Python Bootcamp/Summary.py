# String >>> Int

'''

a = "5"
b = "6"

print(int(a) + int(b))

'''

# String >>> Float

'''

a = "5.5"
b = "6.6"

print(float(a) + float(b))

'''

# Int >>> String

'''

age = 21
print("Ich bin " + str(age) + " Jahre alt")

'''


